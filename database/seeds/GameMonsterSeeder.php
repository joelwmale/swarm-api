    /**
     * Seed the monsters table.
     *
     * @return void
     */
    public function run()
    {
        $monsters = $this->getMonsters();

        // Get all game attributes
        $attributes = GameAttribute::where('game_id', '<', 6)->get();

        foreach ($monsters as $monster) {
            $baseMonster = strlen($monster['game_id']) === 3;

            // @TODO dont really like this, maybe add a flag to the monster array
            // saying its a base monster
            if ($baseMonster) {
                // Create the base monster with an attribute of 0
                GameMonster::create(array_merge($monster, ['attribute_id' => GameAttribute::where('game_id', 6)->first()->id]));

                // Create each attribute
                foreach ($attributes as $attribute) {
                    GameMonster::create([
                        'attribute_id' => $attribute->id,
                        'game_id' => ($monster['game_id']. 0 .$attribute->game_id),
                        'name' => $monster['name'].' ('.$attribute->name.')',
                    ]);
                }
            } else {
                // Evolved monster already has the attribute type
                $attribute = GameAttribute::where('game_id', substr($monster['game_id'], -1))->first();
                GameMonster::create(array_merge($monster, ['attribute_id' => $attribute->id]));
            }
        }
    }

